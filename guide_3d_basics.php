<li>How Does 3D Printing Work?</li>
</ul>
<p>The complete process consists of five stages.</p>
<h2>Step 1: Create a 3D Model</h2>
<p>Everything starts with an idea. The object is designed using CAD software.</p>
<p>Popular software: Fusion 360, TinkerCAD, SolidWorks, Blender</p>
<p>Example: A student designs: Keychain, Mobile Stand, Robot Part on a computer.</p>
<h2>Interactive Idea</h2>
<h2>Show: Sketch ?? 3D Model Animation</h2>
<h2>Step 2: Convert into STL File</h2>
<p>The design software saves the model as:</p>
<p>STL File. STL stands for: Standard Tessellation Language or Stereo Lithography File. The STL file contains</p>
<p>information about the object's shape. Think of STL as a blueprint for the printer.</p>
<h2>Step 3: Slicing</h2>
<p>A printer cannot understand STL directly. The STL file is loaded into slicing software.</p>
<h2>Popular slicers: Cura, PrusaSlicer, OrcaSlicer</h2>
<p>The slicer converts the model into: G-Code. G-Code is the language of the printer.</p>
<p>Example Commands: Move nozzle, Set temperature, Start extrusion, Move X-axis, Move Y-axis</p>
<h2>Interactive Idea</h2>
<p>Show animation: 3D Model ?? Hundreds of Layers ?? G-Code ?? Printer</p>
<h2>Step 4: Printing</h2>
<p>Now the printer starts working. The nozzle heats the filament. Material melts. The nozzle deposits material</p>
<p>layer by layer. Gradually the object appears. Students love seeing this process. Add a time-lapse animation.</p>
<h2>Step 5: Post Processing</h2>
<h2>After printing:</h2>
<h2>Supports are removed</h2>
<h2>Surface is cleaned</h2>
<h2>Sanding may be done</h2>
<h2>Painting may be done</h2>
<p>Now the final product is ready.</p>
