<h2>A. ?? Subtractive Manufacturing</h2>
<h2>What is Subtractive Manufacturing?</h2>
<p>Subtractive Manufacturing is a process in which material is removed from a larger solid block to create the</p>
<p>final shape. Think of a sculptor carving a statue from a stone block. The final product is obtained by removing</p>
<p>unwanted material.</p>
<h2>How Does It Work?</h2>
<p>Start with: ? Solid Metal Block ? Remove Material ?? Machine Desired Shape ?? Finished Product.</p>
<h2>Common Processes</h2>
<p>Milling - A rotating cutting tool removes material.</p>
<h2>Used for:</h2>
<h2>Engine parts</h2>
<h2>Molds</h2>
<h2>Fixtures</h2>
<p>Turning - The workpiece rotates while the cutting tool removes material.</p>
<h2>Used for:</h2>
<h2>Shafts</h2>
<h2>Bolts</h2>
<h2>Cylindrical parts</h2>
<p>Drilling - Creates holes in materials.</p>
<p>Used in almost every manufacturing industry.</p>
<p>CNC Machining - Computer-controlled cutting operations.</p>
<p>Widely used for precision components.</p>
<h2>Real-Life Examples</h2>
<h2>Automobile Industry</h2>
<h2>Engine blocks</h2>
<h2>Transmission components</h2>
<h2>Aerospace Industry</h2>
<h2>Aircraft brackets</h2>
<h2>Turbine components</h2>
<h2>Medical Industry</h2>
<h2>Surgical instruments</h2>
<h2>Advantages</h2>
<p>High Precision - Tolerance can be extremely accurate.</p>
<p>Excellent Surface Finish - Parts often require little finishing.</p>
<p>Strong Parts - Material properties remain intact.</p>
<p>Suitable for Metal Components - Works well with metals and engineering materials.</p>
<h2>Limitations</h2>
<p>Material Waste - Large amounts of material are removed and discarded.</p>
<p>Expensive for Complex Parts - Complex shapes require multiple operations.</p>
<p>Longer Production Time - Machining large components may take hours.</p>
<p>Internal Features Difficult - Hollow structures are difficult to manufacture.</p>
<h2>Fun Fact</h2>
<p>Sometimes 90% of the original material block is removed to manufacture aerospace components.</p>
<h2>B. ?? Formative Manufacturing</h2>
<h2>What is Formative Manufacturing?</h2>
<p>Formative Manufacturing shapes material using molds, dies, pressure, or heat. Instead of removing material, the</p>
<p>material is forced into a desired shape. Think about making ice cubes. Water is poured into a mold. The mold</p>
<p>determines the final shape.</p>
<h2>How Does It Work?</h2>
<p>Material ? Mold or Die ? Pressure / Heat ? Finished Product.</p>
<h2>Common Processes</h2>
<p>Injection Molding - Molten plastic is injected into a mold.</p>
<h2>Used for:</h2>
<h2>Bottles</h2>
<h2>Toys</h2>
<h2>Mobile Covers</h2>
<p>Casting - Molten metal is poured into a mold.</p>
<h2>Used for:</h2>
<h2>Engine blocks</h2>
<h2>Machine bodies</h2>
<p>Forging - Material is compressed under high pressure.</p>
<h2>Used for:</h2>
<h2>Crankshafts</h2>
<h2>Hand tools</h2>
<h2>Industrial components</h2>
<h2>Real-Life Examples</h2>
<p>Look around your room. Most plastic products are manufactured using Injection Molding.</p>
<h2>Examples:</h2>
<h2>Pen bodies</h2>
<h2>Water bottles</h2>
<h2>Keyboard keys</h2>
<h2>Phone covers</h2>
<h2>Advantages</h2>
<p>Very Fast Production - Thousands of parts can be produced daily.</p>
<p>Low Cost per Part - Once the mold is ready, production is economical.</p>
<p>Excellent Repeatability - Every part is almost identical.</p>
<p>Suitable for Mass Production - Ideal for manufacturing millions of units.</p>
<h2>Limitations</h2>
<p>High Mold Cost - A mold may cost thousands or even lakhs of rupees.</p>
<p>Design Changes are Expensive - Even small modifications require mold changes.</p>
<p>Long Development Time - Mold design and manufacturing takes time.</p>
<p>Not Suitable for Low Quantities - Producing only a few parts is expensive.</p>
<h2>Fun Fact</h2>
<p>A plastic bottle cap costing less than ?1 may require a mold worth several lakhs of rupees.</p>
<h2>C. ?? Additive Manufacturing (3D Printing)</h2>
<h2>What is Additive Manufacturing?</h2>
<p>Additive Manufacturing creates objects by adding material layer by layer. Instead of cutting or molding</p>
<p>material, the product is built from the ground up. This technology is commonly known as: 3D Printing.</p>
<h2>How Does It Work?</h2>
<p>3D Model ? Slice into Layers ? Printer Deposits Material ? Finished Product.</p>
<h2>Why is it Called Additive?</h2>
<p>Because material is added only where needed. No unnecessary material is removed.</p>
<h2>Common Technologies</h2>
<p>FDM - Plastic filament printing.</p>
<p>SLA - Liquid resin printing.</p>
<p>SLS - Powder-based printing.</p>
<p>Metal Printing - Laser melts metal powder.</p>
<h2>Real-Life Examples</h2>
<p>Education - Project models.</p>
<p>Healthcare - Customized prosthetics.</p>
<p>Aerospace - Lightweight aircraft components.</p>
<p>Automotive - Rapid prototypes.</p>
<p>Architecture - Building models.</p>
<h2>Advantages</h2>
<p>Minimal Material Waste - Material is used only where required.</p>
<p>Complex Geometries - Internal channels and lattice structures are possible.</p>
<p>Rapid Prototyping - Designs can be tested quickly.</p>
<p>Mass Customization - Every product can be unique.</p>
<p>No Tooling Required - No molds or dies are needed.</p>
<h2>Limitations</h2>
<p>Slower for Mass Production - Printing thousands of parts takes time.</p>
<p>Surface Finish May Require Improvement - Post-processing is often required.</p>
<p>Build Size Limitations - Limited by printer volume.</p>
<p>Material Cost - Some specialized materials are expensive.</p>
<h2>Fun Fact</h2>
<p>NASA uses 3D printing to manufacture rocket engine components with internal cooling channels impossible to</p>
<p>produce using traditional methods.</p>
